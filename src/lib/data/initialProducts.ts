import type { Product } from "@/lib/types";
import { getProductImageSrc } from "@/lib/productImages";

function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "") || "product";
}

/** Product images from src/product pictures (cycled by product index) */
function productImageByIndex(index: number): string {
  return getProductImageSrc(index);
}

function p(
  name: string,
  price: number,
  category: string,
  brand: string,
  compareAtPrice?: number,
  id?: string
): Product {
  const slug = slugify(name);
  return {
    id: id ?? slug,
    slug,
    name,
    shortDescription: name,
    description: `${name}. Best skincare in Bangladesh. From ${brand}.`,
    price,
    compareAtPrice,
    currency: "BDT",
    images: [""], // assigned by assignImages() from index
    skinTypes: ["combination", "dry", "oily"],
    concerns: ["acne", "anti-aging", "hyperpigmentation"],
    ingredients: ["niacinamide"],
    category,
    brand,
    stock: 50,
    membersOnly: false,
  };
}

/** Assign a unique image to each product by index (called when exporting) */
function assignImages(products: Product[]): Product[] {
  return products.map((prod, i) => ({ ...prod, images: [productImageByIndex(i)] }));
}

/** Mark as best new / featured on home page */
function featured(product: Product): Product {
  return { ...product, featured: true };
}

